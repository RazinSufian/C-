#include <stdio.h>
#include<math.h>
int main() {
    //++i pre increment operetor
    //i++ post increment operetor
    // same for minus, --i pre decrement operetor,i-- post decrement operetor
    int i=1;
    printf("%d\n",i++);
    printf("%d\n",i);


    printf("%d\n",++i);
    printf("%d\n",i);
    return 0;
    
}